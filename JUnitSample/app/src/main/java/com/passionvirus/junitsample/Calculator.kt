package com.passionvirus.junitsample

class Calculator {
    companion object {
        fun add(a : Int, b : Int) : Int {
            return a + b
        }

        fun subtract(a : Int, b : Int) : Int {
            return a - b
        }

        fun multiply(a : Int, b : Int) : Int {
            return a * b
        }

        fun division(a : Int, b : Int) : Int {
            return a / b
        }
    }
}